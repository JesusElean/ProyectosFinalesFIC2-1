    private void lblGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblGuardarMouseClicked
         // Validar que se haya ingresado el n√∫mero de cuenta
    if (txtCuenta.getText().trim().isEmpty() || txtCuenta.getText().equals("Ingrese su Cuenta:")) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese el n√∫mero de cuenta.", "Error", JOptionPane.ERROR_MESSAGE);
        txtCuenta.requestFocus();
        return;
    }
    
    int idCuenta;
    try {
        idCuenta = Integer.parseInt(txtCuenta.getText().trim());
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "El n√∫mero de cuenta debe ser un entero v√°lido.", "Error", JOptionPane.ERROR_MESSAGE);
        txtCuenta.requestFocus();
        txtCuenta.selectAll();
        return;
    }
    
    // Validar que se haya ingresado el importe
    if (txtImporte.getText().trim().isEmpty() || txtImporte.getText().equals("Ingrese su Importe:")) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese el importe.", "Error", JOptionPane.ERROR_MESSAGE);
        txtImporte.requestFocus();
        return;
    }
    
    double importe;
    try {
        importe = Double.parseDouble(txtImporte.getText().trim());
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "El importe debe ser un n√∫mero v√°lido.", "Error", JOptionPane.ERROR_MESSAGE);
        txtImporte.requestFocus();
        txtImporte.selectAll();
        return;
    }
    
    // Obtener el tipo de movimiento desde el combo: usamos "Dep√≥sito" y "Retiro"
    String tipoSeleccionado = cbTipoMovimiento.getSelectedItem().toString();
    String tipo;
    if (tipoSeleccionado.equalsIgnoreCase("Dep√≥sito")) {
        tipo = "D";
    } else {
        tipo = "R";
    }
    
    // Nota: El campo de fecha en la base de datos se establece autom√°ticamente (CURRENT_TIMESTAMP)
    // por lo que no es indispensable usar txtFechaMov para la inserci√≥n.
    
    // Crear el objeto Movimiento con los datos ingresados
    Movimiento mov = new Movimiento();
    mov.setIdCuenta(idCuenta);
    mov.setTipo(tipo);
    mov.setCantidad(importe);
    // Si deseas asignar la fecha ingresada manualmente:
    // mov.setFecha(txtFechaMov.getText().trim());
    
    // Intentar insertar el movimiento en la base de datos
    if (mov.insertar()) {
        JOptionPane.showMessageDialog(this, "Movimiento registrado exitosamente.", "√âxito", JOptionPane.INFORMATION_MESSAGE);
        reiniciarCampos(); // Limpiar los campos para un nuevo registro
    } else {
        JOptionPane.showMessageDialog(this, "Ocurri√≥ un error al registrar el movimiento.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_lblGuardarMouseClicked

